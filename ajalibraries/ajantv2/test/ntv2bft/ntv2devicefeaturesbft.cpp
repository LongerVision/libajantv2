static bool NTV2GetNumBFT (void)
{
	const NTV2DeviceIDSet goodDeviceIDs (::NTV2GetSupportedDevices ());
	for (NTV2DeviceIDSetConstIter iter (goodDeviceIDs.begin ());  iter != goodDeviceIDs.end ();  ++iter)
	{
		const NTV2DeviceID	deviceID		(*iter);
		const UWord			numLTCInputs	(::NTV2DeviceGetNumLTCInputs (deviceID));
		const UWord			numLTCOutputs	(::NTV2DeviceGetNumLTCOutputs (deviceID));
		const UWord			numAudioEngines	(::NTV2DeviceGetNumAudioSystems (deviceID));
		for (UWord nLTCInput (0);  nLTCInput <= numLTCInputs;  nLTCInput++)
			SHOULD_BE_EQUAL (::NTV2DeviceCanDoLTCInN (deviceID, nLTCInput), nLTCInput < numLTCInputs);
		for (UWord nLTCOutput (0);  nLTCOutput <= numLTCOutputs;  nLTCOutput++)
			SHOULD_BE_EQUAL (::NTV2DeviceCanDoLTCOutN (deviceID, nLTCOutput), nLTCOutput < numLTCOutputs);
		for (UWord nAudioEngine (0);  nAudioEngine <= numAudioEngines;  nAudioEngine++)
			SHOULD_BE_EQUAL (::NTV2DeviceCanDoAudioN (deviceID, nAudioEngine), nAudioEngine < numAudioEngines);
	}
	return true;
}	//	NTV2GetNumBFT



bool NTV2DeviceFeaturesBFT (void)
{
	SHOULD_BE_TRUE (::NTV2DeviceCanDoVideoFormatBFT ());
	SHOULD_BE_TRUE (::NTV2GetNumBFT ());
	return true;

}	//	NTV2DeviceFeaturesBFT
